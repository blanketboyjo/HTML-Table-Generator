    private void m_btnAddRowActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_m_btnAddRowActionPerformed
        // TODO add your handling code here:
        
        m_name      .add(index, JOptionPane.showInputDialog("What shall I call this item?"));
        m_price     .add(index, JOptionPane.showInputDialog("If I may, how much did it cost you?"));
        m_link      .add(index, JOptionPane.showInputDialog("A link to this product would be much apprieciated."));
        m_comment   .add(index, JOptionPane.showInputDialog("Do you have any comments on the product?"));
        
//        System.out.println(m_name .get(index));
//        System.out.println(m_price.get(index));
//        System.out.println(m_link .get(index));
        
        if(m_comment.get(index).toUpperCase().equals("NO")){
            m_comment.set(index, "");
        }
        
        m_listModel.add(index, m_name.get(index));
        index++;
    }//GEN-LAST:event_m_btnAddRowActionPerformed

    private void m_btnDeleteRowActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_m_btnDeleteRowActionPerformed
        // TODO add your handling code here:
        int selectedIndex = m_list.getSelectedIndex();
        if (selectedIndex != -1) {
            m_listModel .remove(selectedIndex);
            m_name      .remove(selectedIndex);
            m_price     .remove(selectedIndex);
            m_link      .remove(selectedIndex);
            m_comment   .remove(selectedIndex);            
        } else {
//            System.out.println("Nothing selected");
        }
    }//GEN-LAST:event_m_btnDeleteRowActionPerformed

