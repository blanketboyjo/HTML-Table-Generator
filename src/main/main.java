    private void m_displayVariables(int selectedIndex){
        if(-1 != selectedIndex){
            m_textBoxName   .setText(m_name.get(selectedIndex));
            m_textBoxPrice  .setText(m_price.get(selectedIndex));
            m_textBoxLink   .setText(m_link.get(selectedIndex));
            m_textBoxComment.setText(m_comment.get(selectedIndex));
            m_currentIndex = selectedIndex;
            m_listSelected  = true;
        }else{
            m_textBoxName.setText("");
            m_listSelected = false;
        }
    }
    
    private void m_setEditable(boolean answer){
        m_textBoxName   .setEditable(answer);
        m_textBoxPrice  .setEditable(answer);
        m_textBoxLink   .setEditable(answer);
        m_textBoxComment.setEditable(answer);
        
        m_textBoxName   .setFocusable(answer);
        m_textBoxPrice  .setFocusable(answer);
        m_textBoxLink   .setFocusable(answer);
        m_textBoxComment.setFocusable(answer);
        m_btnUpdate.setEnabled(answer && m_listSelected);
        m_isEditable = answer;
        if(!answer){
            m_displayVariables(m_list.getSelectedIndex());
        }
    }
    
    private void m_toggleEditable(){
       m_isEditable = !m_isEditable;
       m_setEditable(m_isEditable);
    }
    
    
